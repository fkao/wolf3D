#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	size_t	l2;
	char	*ptr1;

	l2 = ft_strlen(s2);
	ptr1 = s1 + ft_strlen(s1);
	ft_memcpy(ptr1, s2, l2 + 1);
	*(ptr1 + l2) = '\0';
	return (s1);
}
