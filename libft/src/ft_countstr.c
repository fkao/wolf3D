#include "libft.h"

size_t	ft_countstr(char const *s, char c)
{
	size_t	len;

	len = 0;
	while (*s)
	{
		if (*s != c && (*(s + 1) == c || *(s + 1) == '\0'))
			len++;
		s++;
	}
	return (len);
}
