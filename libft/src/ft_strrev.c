#include "libft.h"

char	*ft_strrev(char *s)
{
	char	*p1;
	char	*p2;
	char	tmp;

	if (!s || !*s)
		return (NULL);
	p1 = s;
	p2 = s + ft_strlen(s) - 1;
	while (p1 < p2)
	{
		tmp = *p1;
		*p1 = *p2;
		*p2 = tmp;
		p1++;
		p2--;
	}
	return (s);
}
